from math import radians, sin, cos, tan
a = float(input('Set an angle: '))
sine = sin(radians(a))
print('The angle of {} has SINE of {:.2f}'.format(a, sine))
cosine = cos(radians(a))
print('The angle of {} has COSINE of {:.2f}'.format(a, cosine))
tangent = tan(radians(a))
print('The angle of {} has a TANGENT of {:.2f}'.format(a, tangent))


