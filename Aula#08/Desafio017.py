from math import hypot
co = float(input('Length of opposite side: '))
ca = float(input('Length of adjacent side: '))
hi = hypot(co, ca)
print('The hypotenuse will measure {:.2f}'.format(hi))

import math
co = float(input('Length of opposite side: '))
ca = float(input('Length of adjacent side: '))
hi = math.hypot(co, ca)
print('The hypotenuse will measure {:.2f}'.format(hi))

