phrase = str(input('Enter a phrase: ')).upper().strip()
print('The letter A appears {} times in the sentence'.format(phrase.count('A')))
print('The first letter A appeared at position {}'.format(phrase.find('A')))
print('The last letter A appeared at position {}'.format(phrase.rfind('A')))


